"""
Calcular el área y perímetro de un rectángulo.
"""
altura=int(input("inserte la altura del rectangulo: "))
ancho=int(input("inserte la anchura del rectangulo: "))
area= altura*ancho
perimetro= (altura*2)+(ancho*2)
print("El area del rectangulo es ", area)
print("El perimetro del rectanguloes ", perimetro)
